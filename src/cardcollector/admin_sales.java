    private void View_DetailbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_View_DetailbtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)SALESTable.getModel();
        int Myindex = SALESTable.getSelectedRow();
        if(Myindex == -1)
        {
            JOptionPane.showMessageDialog(this,"Select Sales record to view details");
        }else
        {
            String sale_id = model.getValueAt(Myindex,0).toString();
            SelectSalesDetails(sale_id);
            Popup_details.pack();
            Popup_details.setLocationRelativeTo(null);
            Popup_details.setVisible(true);
        }
    }//GEN-LAST:event_View_DetailbtnActionPerformed

