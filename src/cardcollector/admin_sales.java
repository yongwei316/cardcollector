    public void SelectSales()
    {
        try{
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/CardCollectordb","User1","user1");
            St = Con.createStatement();
            Rs = St.executeQuery("select * from User1.SALETB");
            SALESTable.setModel(DbUtils.resultSetToTableModel(Rs));
        }catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    public void SelectSalesDetails(String sale_id)
    {
        try{
            DefaultTableModel model = (DefaultTableModel)DetailTable.getModel();
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/CardCollectordb","User1","user1");
            PreparedStatement add = Con.prepareStatement("select * from User1.SALEPROTB where SALEID=?");
            add.setString(1,sale_id);
            Rs = add.executeQuery();
            DetailTable.setModel(DbUtils.resultSetToTableModel(Rs));
        }catch(Exception e)
        {
            e.printStackTrace();
        }
    }
